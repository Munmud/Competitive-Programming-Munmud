long long tree[1000001*3];
class seg
{
private :
    int n ;

    void init(int node, int b, int e , vector <int>& arr) ;
    long long quer(int node, int b, int e, int i, int j) ;
    void updt(int node, int b, int e, int i, int newvalue) ;
public :

    // i থেকে j তম (ইনক্লুসিভ) sum বের করি
    long long query(int i , int j){
        return quer(1,1,n,i+1,j+1) ;
    }

     // idx তম ইনডেক্সের ভ্যালু চেঞ্জ করে newvalue রাখি
    void update(int idx, int newvalue){
        return updt(1,1,n,idx+1,newvalue) ;
    }
    
    seg(vector <int>& arr) ;
};

seg::seg( vector <int>& arr )
{
    n = arr.size() ;
    init(1,1,n,arr) ;
}

void seg::init(int node, int b, int e , vector <int>& arr )
{
    if (b == e) {
        tree[node] = arr[b-1];
        return;
    }
    int Left = node * 2;
    int Right = node * 2 + 1;
    int mid = (b + e) / 2;
    init(Left, b, mid , arr );
    init(Right, mid + 1, e , arr );
    tree[node] = min( tree[Left] , tree[Right] ) ;
}

long long seg::quer(int node, int b, int e, int i, int j)
{
    if (i > e || j < b) return INT_MAX; 
    if (b >= i && e <= j) return tree[node]; 
        
    int Left = node * 2; 
    int Right = node * 2 + 1;
    int mid = (b + e) / 2;
    long long p1 = quer(Left, b, mid, i, j) ;
    long long p2 = quer(Right, mid + 1, e, i, j) ;
    return  min(p1,p2) ; 
}

void seg::updt(int node, int b, int e, int i, int newvalue)
{
    if (i > e || i < b) return; 
    if (b >= i && e <= i) { 
        tree[node] = newvalue; 
        return;
    }
    int Left = node * 2; 
    int Right = node * 2 + 1;
    int mid = (b + e) / 2;
    updt(Left, b, mid, i, newvalue);
    updt(Right, mid + 1, e, i, newvalue);
    tree[node] =  min( tree[Left] , tree[Right] );
}


