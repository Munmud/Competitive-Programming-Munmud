ll dist_max[200005] ;
ll dist_min[200005] ;
int k ;
bool taken[200005] ;

void go(int node)
{
    taken[node] = 1 ;
    // wa(node) ;

    int cnt = 0 ;

    for (auto i : v[node])
    {
        if (taken[i]) continue ;
        cnt++ ;
        // wa2(node , i) ;
        go(i) ;
        dist_max[node] = MAX(dist_max[node] , dist_max[i] +1 ) ; 
        dist_min[node] = MIN(dist_min[node] , dist_min[i] +1 ) ; 

        // wa2(dist_max[node] , dist_min[node]) ;
    }
    if (cnt == 0){
        dist_max[node] = 0 ;
        dist_min[node] = 0 ;
    }
} 

void visit(int node)
{
    taken[node] = 1 ;

    vector <PII> pp ;
    for (auto i : v[node])
    {
        if (taken[i]) continue ;
        pp.push_back({i , dist_min[i]}) ;
    }
    sort(ALL(pp) , [] (PII a , PII b)
    {
        return a.yy > b.yy ;
    }) ;
    int dd = 0 ;
    for (auto i : pp){
        k = MAX(k , dd) ;
        dd = dist_min[i.xx] +2 ;
        visit(i.xx) ;
    }
}


